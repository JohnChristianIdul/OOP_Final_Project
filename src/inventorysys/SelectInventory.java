    public void displayTable(){
        ArrayList<Inventory> it = conn.displayInventory();
        for(Inventory i : it){
            String data[]={Integer.toString(i.getInventoryID()), i.getInventoryName()};
            tbl.addRow(data);
        }
    }
    
