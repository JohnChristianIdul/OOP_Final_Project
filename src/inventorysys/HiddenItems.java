    public void displayTable(String inventoryName){
        ArrayList<Item> item = conn.hiddenItems(inventoryName);
        for(Item i : item){
            String data[]={Integer.toString(i.getItemID()),i.getItemName(), i.getDescription(), Integer.toString(i.getQuantity())};
            tbl.addRow(data);
        }
    }
    
    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        // TODO add your handling code here:
        String Name = tfItemName.getText();
        String description = taDescription.getText();
        int quantity = Integer.parseInt(tfQuantity.getText());
        Item i = new Item(Name,quantity,description);
        conn.showItem(i, inventory);
        tfItemName.setText("");
        taDescription.setText("");
        tfQuantity.setText("");
        refreshTable();
    }//GEN-LAST:event_btnShowActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        InventoryItem showed = new InventoryItem(inventory, inventoryid);
        showed.show();
        this.dispose();
    }//GEN-LAST:event_btnBackActionPerformed

    private void tblHiddenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHiddenMouseClicked
        // TODO add your handling code here:
        int index = tblHidden.getSelectedRow();
        String Name = (String) tblHidden.getValueAt(index,1);
        String description = (String) tblHidden.getValueAt(index,2);
        String Quantity = (String) tblHidden.getValueAt(index,3);
        tfItemName.setText(Name);
        tfQuantity.setText(Quantity);
        taDescription.setText(description);
    }//GEN-LAST:event_tblHiddenMouseClicked

    public void refreshTable(){
        tbl.setRowCount(0);
        displayTable(inventory);
    }
    
